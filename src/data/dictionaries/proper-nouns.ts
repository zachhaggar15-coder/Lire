function labelForKind(kind: ProperKind): string {
  if (kind === "place") return "proper noun (place)";
  if (kind === "person") return "proper noun (person)";
  if (kind === "institution") return "proper noun (institution)";
  if (kind === "media") return "proper noun (media)";
  return "proper noun (language)";
}

function derivedForms(seed: ProperNounSeed): string[] {
  if (seed.kind !== "person") return [];

  const parts = seed.name.split(/\s+/).filter(Boolean);
  if (parts.length < 2) return [];

  const particleStarts = new Set(["de", "du", "des", "le", "la", "van", "von"]);
  const surnameStart = particleStarts.has(parts.at(-2)?.toLowerCase() ?? "") ? parts.length - 2 : parts.length - 1;

  return [parts.slice(surnameStart).join(" ")];
}

function entryFromSeed(seed: ProperNounSeed): DictionaryEntry {
  const forms = [...new Set([...(seed.forms ?? []), ...derivedForms(seed)])];

  return {
    lemma: seed.name,
    forms: forms.length ? forms : undefined,
    translations: [seed.translation ?? seed.name],
    partOfSpeech: labelForKind(seed.kind),
    notes: "Proper noun: kept as a name/place rather than treated as an unknown dictionary word.",
  };
}

const seenProperNames = new Set<string>();

export const properNounDictionary: DictionaryEntry[] = properNounSeeds
  .filter((seed) => {
    const key = seed.name.toLowerCase();
    if (seenProperNames.has(key)) return false;
    seenProperNames.add(key);
    return true;
  })
  .map(entryFromSeed);
