import type { Cefr, DictionaryEntry } from "@/lib/dictionary/types";

function phrase(
  lemma: string,
  translations: string[],
  partOfSpeech: string,
  cefr: Cefr,
  forms: string[] = [],
  fr?: string,
  en?: string
): DictionaryEntry {
  return {
    lemma,
    forms,
    translations,
    partOfSpeech,
    cefr,
    examples: fr && en ? [{ fr, en }] : undefined,
  };
}

